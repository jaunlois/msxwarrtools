import * as XLSX from 'xlsx';
import { ReportData, SBIInvoice, AgeAnalysisClaim } from './types';
import { normalizeRO } from './parsePDF';

export function generateWeeklyReport(data: ReportData): ArrayBuffer {
  const wb = XLSX.utils.book_new();
  createWIPSheet(wb, data);
  createAgeAnalysisSheet(wb, data);
  createDmsSheet(wb, data);
  createPartsReturnSheet(wb);
  createPlaceholderSheet(wb, 'DAS', ['Snip of DAS']);
  createScrapListSheet(wb);
  createTrainingSheet(wb);
  return XLSX.write(wb, { bookType: 'xlsx', type: 'array' });
}

