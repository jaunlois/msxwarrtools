export function generateSBPayments(sbiInvoices: SBIInvoice[], claims: AgeAnalysisClaim[]): ArrayBuffer {
  const wb = XLSX.utils.book_new();
  const roToInvoice = new Map<string, string>();
  for (const c of claims) {
    const norm = normalizeRO(c.roNumber);
    if (c.invoiceNo) roToInvoice.set(norm, c.invoiceNo);
  }

  for (const sbi of sbiInvoices) {
    const rows: any[][] = [
      ['(SBI) SELF BILLING TAX INVOICE'], [],
      ['GEOGRAPHICAL SALES AREA:', 'ZAF', '', '', 'DATE OF ISSUE:', sbi.dateOfIssue],
      ['CURRENCY:', 'ZAR', '', '', 'INVOICE NUMBER:', `91106-${sbi.invoiceNumber}`],
      [], ['SUPPLIER', '', '', '', 'CUSTOMER'],
      ['Mortimer Motors (Pty) Ltd', '', '', '', 'FORD MOTOR CO. OF SOUTHERN AFRICA'],
      [], [],
      ['SERVICES PERFORMED BY THE DEALER IN CONNECTION WITH WARRANTY REPAIRS'], [],
      ['REPAIR ORDER NO.', 'Claim Type', 'Sub Code', 'VIN', 'DATE OF REPAIR', 'NET AMOUNT', 'VAT RATE %', 'VAT AMOUNT', 'TOTAL (INCLUDING VAT)', 'INVOICE NO.'],
    ];
    let totalNet = 0, totalVat = 0, totalInc = 0;
    for (const sc of sbi.claims) {
      const normRO = normalizeRO(sc.repairOrderNo);
      totalNet += sc.netAmount; totalVat += sc.vatAmount; totalInc += sc.totalIncVAT;
      rows.push([sc.repairOrderNo, sc.claimType, sc.subCode, sc.vin, sc.dateOfRepair, sc.netAmount, sc.vatRate, sc.vatAmount, sc.totalIncVAT, roToInvoice.get(normRO) || '']);
    }
    rows.push([], ['TOTAL', '', '', '', '', totalNet, '', totalVat, totalInc, '']);
    rows.push([], ['GRAND TOTAL', '', '', '', '', '', '', '', sbi.grandTotal || totalInc]);
    const ws = XLSX.utils.aoa_to_sheet(rows);
    ws['!cols'] = [{ wch: 20 }, { wch: 12 }, { wch: 10 }, { wch: 20 }, { wch: 16 }, { wch: 14 }, { wch: 12 }, { wch: 14 }, { wch: 20 }, { wch: 16 }];
    XLSX.utils.book_append_sheet(wb, ws, (sbi.invoiceNumber || 'SBI').substring(0, 31));
  }
  return XLSX.write(wb, { bookType: 'xlsx', type: 'array' });
}
